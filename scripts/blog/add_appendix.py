def build_appendix(d):
    yrs = d["years"]
    s = "\n---\n\n## 부록: 재무제표 3년 — dartlab 실측\n\n"
    s += '```python\nimport dartlab\nc = dartlab.Company("{}")\nc.show("IS", freq="Y")\nc.show("BS", freq="Y")\nc.show("CF", freq="Y")\n```\n\n'
    s += "### 손익계산서 (IS) — 단위 억원\n\n"
    s += "| 항목 | " + " | ".join(str(y) for y in yrs) + " |\n"
    s += "|---|" + "".join(["---:|" for _ in yrs]) + "\n"
    for row in d["IS"]:
        s += "| " + row[0] + " | " + " | ".join(row[1:]) + " |\n"
    s += "\n### 재무상태표 (BS) — 단위 억원\n\n"
    s += "| 항목 | " + " | ".join(str(y) for y in yrs) + " |\n"
    s += "|---|" + "".join(["---:|" for _ in yrs]) + "\n"
    for row in d["BS"]:
        s += "| " + row[0] + " | " + " | ".join(row[1:]) + " |\n"
    s += "\n### 현금흐름표 (CF) — 단위 억원\n\n"
    s += "| 항목 | " + " | ".join(str(y) for y in yrs) + " |\n"
    s += "|---|" + "".join(["---:|" for _ in yrs]) + "\n"
    for row in d["CF"]:
        s += "| " + row[0] + " | " + " | ".join(row[1:]) + " |\n"
    return s


